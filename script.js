  function renderSideMenuBar() {
    const sideMenuBar = document.getElementById("side-menu-bar");
    for (let index = 0; index < juz.length; index++) {
      const button = document.createElement("button");
      button.innerText = juz[index].title;
      button.addEventListener("click", () => {
        juz[index].callback(juz[index].title, juz[index].key);
      });
      sideMenuBar.appendChild(button);
    }
  }

  // DropDownBar

  function toggleDropdown() {
    var dropdownContent = document.getElementById("dropdownContent");
    dropdownContent.classList.toggle("show");
}

// Close the dropdown if the user clicks outside of it
window.onclick = function(event) {
    if (!event.target.matches('.dropdown')) {
        var dropdowns = document.getElementsByClassName("dropdown-content");
        for (var i = 0; i < dropdowns.length; i++) {
            var openDropdown = dropdowns[i];
            if (openDropdown.classList.contains('show')) {
                openDropdown.classList.remove('show');
            }
        }
    }
}
