  function getQuran(juzTitle, juzName) {
    const ayahs = ayah[juzName]["ayyahs"];
    const audioPath = ayah[juzName]["audio"];
    const cardHeader = document.getElementById("card-header");
  
    cardHeader.innerHTML = juzTitle;
  
    const cardContent = document.getElementById("card-content");
    let content = "";
    for (let index = 0; index < ayahs.length; index++) {
      content += `<li>${ayahs[index]}</li>`;
    }
    cardContent.innerHTML = content;
  
    // add Audio Child
    const audio = document.createElement("audio");
    audio.controls = true;
    audio.src = `${audioPath}`;
    cardContent.appendChild(audio);
  }
